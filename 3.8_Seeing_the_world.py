# Places to visit

places = ['japan','australia','norway', 'belarus']
print(places)
print(sorted(places))
print(places)
# you can add arguments to the function using a comma
# Used sorted() to print in reverse alphabetical order without changing the original list.
print(sorted(places,reverse=True))
print(places)
print('Used reverse()')
places.reverse()
print(places)
places.reverse()
print(places)
print('Used sort()')
places.sort()
print(places)
places.sort(reverse=True)
print(places)


